def _compute_atr(feat_df):
    try:
        import yfinance as yf
        from datetime import datetime, timedelta
        raw = yf.download("GC=F",
                          start=datetime.today()-timedelta(days=30),
                          end=datetime.today(),
                          auto_adjust=False, progress=False)
        if isinstance(raw.columns, __import__("pandas").MultiIndex):
            raw.columns = raw.columns.get_level_values(0)
        if raw.empty or len(raw) < 5:
            raise ValueError("empty")
        high  = raw["High"].values
        low   = raw["Low"].values
        close = raw["Close"].values
        tr    = []
        for i in range(1, len(high)):
            tr.append(max(high[i]-low[i],
                          abs(high[i]-close[i-1]),
                          abs(low[i]-close[i-1])))
        return round(sum(tr[-14:])/min(14, len(tr)), 2)
    except Exception:
        if feat_df is not None and "Close_Returns" in feat_df.columns:
            avg_ret = abs(feat_df["Close_Returns"].tail(14)).mean()
            close   = float(feat_df["Close_XAUUSD"].iloc[-1])
            return round(avg_ret * close, 2)
        return 30.0


def _fetch_live_open():
    """Fetch today's opening candle price to compare against yesterday's levels."""
    try:
        import yfinance as yf
        from datetime import datetime, timedelta
        now   = datetime.utcnow()
        start = now.replace(hour=0, minute=0, second=0, microsecond=0)
        if start >= now:
            start = start - timedelta(days=1)
        raw = yf.download("GC=F", start=start, end=now,
                          interval="1h", auto_adjust=False, progress=False)
        if isinstance(raw.columns, __import__("pandas").MultiIndex):
            raw.columns = raw.columns.get_level_values(0)
        if raw.empty:
            return None, None, None
        open_p  = float(raw["Open"].iloc[0])
        current = float(raw["Close"].iloc[-1])
        high    = float(raw["High"].max())
        low     = float(raw["Low"].min())
        return open_p, current, (high, low)
    except Exception:
        return None, None, None


def _yesterday_review(signal_data, smc_data):
    """Compare yesterday's signal and levels to today's opening — give Qwen context on how it did."""
    open_p, live_price, day_range = _fetch_live_open()
    sig   = signal_data.get("signal", "NO SIGNAL")
    close = signal_data.get("close", 0)

    lines = []
    if open_p:
        gap = open_p - close
        lines.append(f"Yesterday close: ${close:,.2f} | Today open: ${open_p:,.2f} | Gap: {gap:+.1f}")
    if live_price:
        lines.append(f"Current live price: ${live_price:,.2f}")
    if day_range:
        lines.append(f"Today's range so far: ${day_range[1]:,.1f} – ${day_range[0]:,.1f}")

    if open_p and sig in ("BUY", "SELL"):
        moved = open_p - close
        if sig == "BUY":
            lines.append(f"Signal was BUY at ${close:,.2f}. Price opened {'higher' if moved > 0 else 'lower'} by ${abs(moved):.1f}.")
            if live_price:
                from_close = live_price - close
                lines.append(f"From signal close to now: {from_close:+.1f} ({'positive — signal working' if from_close > 0 else 'negative — signal under pressure'})")
        else:
            lines.append(f"Signal was SELL at ${close:,.2f}. Price opened {'lower' if moved < 0 else 'higher'} by ${abs(moved):.1f}.")
            if live_price:
                from_close = live_price - close
                lines.append(f"From signal close to now: {from_close:+.1f} ({'negative — signal working' if from_close < 0 else 'positive — signal under pressure'})")

    if open_p and smc_data:
        res = sorted([l for l in smc_data if l["price"] > close], key=lambda x: x["price"])
        sup = sorted([l for l in smc_data if l["price"] < close], key=lambda x: x["price"], reverse=True)
        if res:
            nearest_r = res[0]["price"]
            lines.append(f"Nearest resistance was ${nearest_r:,.1f} — price {'reached it' if (day_range and day_range[0] >= nearest_r) else 'has not reached it yet'}")
        if sup:
            nearest_s = sup[0]["price"]
            lines.append(f"Nearest support was ${nearest_s:,.1f} — price {'tested it' if (day_range and day_range[1] <= nearest_s) else 'has not tested it'}")

    return "\n".join(lines) if lines else "Live price data not available — market may not be open yet."


