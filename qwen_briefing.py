def run_briefing(signal_data, smc_levels, status_callback=None):
    date_str = signal_data.get("date", datetime.now().strftime("%Y-%m-%d"))

    cached = _load_briefing_cache(date_str)
    if cached:
        if status_callback:
            status_callback("Loaded from today's cache — no API call needed")
        return cached, {}

    if status_callback:
        status_callback("Fetching headlines...")
    headlines = fetch_all_headlines()

    if status_callback:
        status_callback("Searching web...")
    snips     = _ddg_search(f"gold XAU outlook {date_str}")
    web_block = "\n".join(f"- {s}" for s in snips) if snips else ""

    if status_callback:
        status_callback("Calling Qwen3-235B...")

    user_msg = _briefing_user_msg(signal_data, smc_levels, headlines, web_block)
    messages = [
        {"role": "system", "content": BRIEFING_SYSTEM},
        {"role": "user",   "content": user_msg},
    ]
    raw = _call_qwen(messages, max_tokens=1400)

    try:
        result = json.loads(raw)
    except Exception:
        result = {"error": "JSON parse failed", "raw": raw[:300]}

    if "error" not in result:
        wv = result.get("world_view_update", "")
        if wv:
            _write_world_view(wv)
        _append_daily(
            f"\n### {datetime.now().strftime('%H:%M')}\n"
            f"Signal={signal_data.get('signal')} prob={signal_data.get('prob',0):.3f} "
            f"close=${signal_data.get('close',0):,.2f}\n"
            f"{result.get('run_note','')}\n"
        )
        log = _load_signal_log()
        log.append({
            "date":    date_str,
            "signal":  signal_data.get("signal", "NO SIGNAL"),
            "prob":    round(signal_data.get("prob", 0), 4),
            "outcome": "pending",
            "note":    result.get("run_note", ""),
        })
        _save_signal_log(log)
        _save_briefing_cache(date_str, result)

    return result, headlines


