def run_scenario(signal_data, smc_levels, feat_df=None, status_callback=None, force=False):
    date_str = signal_data.get("date", datetime.now().strftime("%Y-%m-%d"))

    if not force:
        cached = _load_scenario_cache(date_str)
        if cached:
            if status_callback:
                status_callback("Loaded scenario from today's cache")
            return cached

    if status_callback:
        status_callback("Fetching live opening price...")
    open_p, live_price, _ = _fetch_live_open()
    if live_price:
        signal_data = dict(signal_data)
        signal_data["live_price"] = live_price

    if status_callback:
        status_callback("Computing ATR...")
    atr = _compute_atr(feat_df)

    if status_callback:
        status_callback("Searching web for live gold price and news...")
    snips  = _ddg_search(f"XAU USD gold price right now {datetime.now().strftime('%B %Y')}")
    snips += _ddg_search(f"gold trade outlook {date_str}")

    if status_callback:
        status_callback("Fetching headlines...")
    headlines = fetch_all_headlines()

    if status_callback:
        status_callback("Calling Qwen3-235B scenario builder...")

    user_msg = _build_scenario_msg(signal_data, smc_levels, atr, headlines, snips)
    messages = [
        {"role": "system", "content": SCENARIO_SYSTEM},
        {"role": "user",   "content": user_msg},
    ]
    raw = _call_qwen(messages, max_tokens=1800)

    try:
        result = json.loads(raw)
    except Exception:
        result = {"error": "JSON parse failed", "raw": raw[:400]}

    if "error" not in result:
        result["_atr"]       = atr
        result["_generated"] = datetime.now().strftime("%H:%M")
        _save_scenario_cache(date_str, result)

    return result


