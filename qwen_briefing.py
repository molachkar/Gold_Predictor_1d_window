def _ddg_search(query, max_results=4):
    try:
        url = f"https://api.duckduckgo.com/?q={requests.utils.quote(query)}&format=json&no_html=1&skip_disambig=1"
        r   = requests.get(url, timeout=8, headers={"User-Agent": "Mozilla/5.0"})
        data = r.json()
        out  = []
        if data.get("AbstractText"):
            out.append(data["AbstractText"][:300])
        for t in data.get("RelatedTopics", [])[:max_results]:
            if isinstance(t, dict) and t.get("Text"):
                out.append(t["Text"][:200])
        return out
    except Exception:
        return []

def _fetch_headlines(url, max_items=4):
    try:
        req = Request(url, headers={"User-Agent": "Mozilla/5.0"})
        with urlopen(req, timeout=6) as resp:
            raw = resp.read()
        root    = ET.fromstring(raw)
        channel = root.find("channel")
        if channel is None:
            return []
        items  = []
        cutoff = datetime.utcnow() - timedelta(days=5)
        for item in channel.findall("item")[:max_items * 2]:
            title   = (item.findtext("title") or "").strip()
            pub_raw = (item.findtext("pubDate") or "").strip()
            if not title:
                continue
            try:
                pub_dt = datetime.strptime(pub_raw, "%a, %d %b %Y %H:%M:%S %z").replace(tzinfo=None)
                if pub_dt < cutoff:
                    continue
            except Exception:
                pass
            items.append(title)
            if len(items) >= max_items:
                break
        return items
    except Exception:
        return []

def fetch_all_headlines():
    return {a: _fetch_headlines(u) for a, u in NEWS_FEEDS.items()}


